package org.wso2.carbon.rssmanager.core.description;

public class DatabasePrivilege {

    private String privName;

    private String privValue;

    public DatabasePrivilege(String privName, String privValue) {
        this.privName = privName;
        this.privValue = privValue;
    }

    public DatabasePrivilege() {

    }

    public String getPrivName() {
        return privName;
    }

    public void setPrivName(String privName) {
        this.privName = privName;
    }

    public String getPrivValue() {
        return privValue;
    }

    public void setPrivValue(String privValue) {
        this.privValue = privValue;
    }
}
